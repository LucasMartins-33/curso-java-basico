package cursojava.Aula34.exercicios;

public class ConversaoDeUnidadesDeVolume {

    public static double litrosParaCentimetros(double num){
        return num * 1000;
    }

    public static double cubicosParaLitros(double num){
        return num * 1000;
    }

    public static double cubicoParaPes(double num){
        return num * 35.32;
    }

    public static double galaoParaPolegadas(double num){
        return num * 231;
    }

    public static double galaoParaLitros(double num){
        return num * 3785;
    }

}
