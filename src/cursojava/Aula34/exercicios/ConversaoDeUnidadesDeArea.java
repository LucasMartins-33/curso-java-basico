package cursojava.Aula34.exercicios;

public class ConversaoDeUnidadesDeArea {

    public static double metrosParaPes(double num){
        return num * 10.76;
    }

    public static double pesParaCentimentros(double num){
        return num * 929;
    }

    public static double milhaParaAcres(double num){
        return num * 640;
    }

    public static double acreParaPes(double num){
        return num * 43560;
    }
}
